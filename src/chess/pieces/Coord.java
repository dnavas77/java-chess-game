public class Coord {
	/**
	 * The x coordinate
	 */
	public int x;

	/**
	 * The y coordinate
	 */
	public int y;
	
	/**
	 * Constructor
	 * @param x The x coordinate
	 * @param y The y coordinate
	 */
	public Coord(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
